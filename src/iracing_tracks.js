export function getAllTracks() {
    const all = [];
    for (const [category, tracks] of Object.entries(IRACING_TRACKS)) {
        for (const track of tracks) {
            all.push({ ...track, category });
        }
    }
    return all;
}

export function getTracksByCategory(category) {
    return IRACING_TRACKS[category] || [];
}

